#   Main Types

str = 'Hello'                                                   # str
int = 10                                                        # int
float = 10.10                                                   # float
bool = True                                                     # bool
list = [str, int, float, bool]                                  # list
dict = {'str': str, 'int': int, 'float': float, 'bool': bool}   # dict
tuple = (str, int, float, bool)                                 # tuple
set = {str, int, float, bool}                                   # set

print('str =', str)
print('int =', int)
print('float =', float)
print('bool =', bool)
print('list =', list)
print('dict =', dict)
print('tuple =', tuple)
print('set =', set)
