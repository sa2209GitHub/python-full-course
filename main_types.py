#   Main Types

str = 'Hello'                                               # str
int = 10                                                    # int
bool = True                                                 # bool
list = [1, 2, 3, str, int, bool]                            # list
dict = {'str': str, 'int': int, 'bool': bool}               # dict

print('str =' , str)
print('int =' , int)
print('bool =', bool)
print('list =', list)
print('dict =', dict)